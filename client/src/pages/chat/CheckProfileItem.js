function removeSpace(inpTxt) {
    const str = inpTxt.split('');
    str.forEach((item, index) => {
     const charCode = item.charCodeAt(0);
     if (charCode === 32 || charCode === 8204) {
       str[index] = '';
     }
    });
    inpTxt = str.join('');
    return inpTxt;
}
export const check = (realVal, predictVal, type)=>{
  const prediction = removeSpace(predictVal)
  if(removeSpace(realVal)==prediction){
    return 1
  }else{
    var allElemsList = hobbyLists
    if(type=='job'){allElemsList = jobLists}
    else if(type=='resistance'){allElemsList = provinceList}
    for (let i = 0; i < allElemsList.length; i++) {
      var subElemes = allElemsList[i].split('،');
      if(subElemes[0].trim()==realVal.trim()){
        for (let j = 0; j < subElemes.length; j++) {
          var elm = subElemes[j]
          const noSpaceElm = removeSpace(elm)
          if(noSpaceElm==prediction){return 1}
          else if(prediction.includes(noSpaceElm)){return 1}
        }
        return 0;
      }
    }
    return 0
  }
}

